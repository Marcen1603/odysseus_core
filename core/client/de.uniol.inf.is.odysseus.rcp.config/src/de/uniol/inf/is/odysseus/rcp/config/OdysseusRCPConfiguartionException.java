package de.uniol.inf.is.odysseus.rcp.config;

public class OdysseusRCPConfiguartionException extends Exception {

	private static final long serialVersionUID = 1L;

	public OdysseusRCPConfiguartionException() {
		super();
	}

	public OdysseusRCPConfiguartionException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public OdysseusRCPConfiguartionException(String message, Throwable cause) {
		super(message, cause);
	}

	public OdysseusRCPConfiguartionException(String message) {
		super(message);
	}

	public OdysseusRCPConfiguartionException(Throwable cause) {
		super(cause);
	}
}
