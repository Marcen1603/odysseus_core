package de.uniol.inf.is.odysseus.peer.distribute;

public class QueryDistributionPostProcessorException extends Exception {

	private static final long serialVersionUID = 1L;

	public QueryDistributionPostProcessorException() {
		super();
	}

	public QueryDistributionPostProcessorException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public QueryDistributionPostProcessorException(String message, Throwable cause) {
		super(message, cause);
	}

	public QueryDistributionPostProcessorException(String message) {
		super(message);
	}

	public QueryDistributionPostProcessorException(Throwable cause) {
		super(cause);
	}

	
}
