package de.uniol.inf.is.odysseus.rcp.editor.script;

public class VisualOdysseusScriptException extends Exception {

	private static final long serialVersionUID = 1L;

	public VisualOdysseusScriptException() {
		super();
	}

	public VisualOdysseusScriptException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public VisualOdysseusScriptException(String message, Throwable cause) {
		super(message, cause);
	}

	public VisualOdysseusScriptException(String message) {
		super(message);
	}

	public VisualOdysseusScriptException(Throwable cause) {
		super(cause);
	}
}
