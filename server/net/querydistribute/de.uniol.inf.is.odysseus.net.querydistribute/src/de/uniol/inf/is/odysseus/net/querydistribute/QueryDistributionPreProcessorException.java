package de.uniol.inf.is.odysseus.net.querydistribute;

import de.uniol.inf.is.odysseus.net.OdysseusNetException;

public class QueryDistributionPreProcessorException extends OdysseusNetException {

	private static final long serialVersionUID = 1L;

	public QueryDistributionPreProcessorException() {
		super();
	}

	public QueryDistributionPreProcessorException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public QueryDistributionPreProcessorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public QueryDistributionPreProcessorException(String arg0) {
		super(arg0);
	}

	public QueryDistributionPreProcessorException(Throwable arg0) {
		super(arg0);
	}
}
